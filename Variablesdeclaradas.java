package relacion1ejercicio1;
public class Variablesdeclaradas {
    public static void main(String[] args) {
        
        //Apartado a//
        System.out.println("Declarando las variables p y q");

        int p = 2;
        int q = 5;

        System.out.println(p);
        System.out.println(q);

        //Apartado b//
        System.out.println("Declarando las variables x, y, z");

        float x = 2.5f;
        float y = 5.6f;
        float z = 7.8f;

        System.out.println(x);
        System.out.println(y);
        System.out.println(z);

        //Apartado c//
        System.out.println("Declarando las variables a, b, c");

        char a = 'l';
        char b = 'j';
        char c = 'g';
    
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);

        //Apartado d//
        System.out.println("Declarando las variables raiz1, raiz2");

        Double raiz1 = 4d;
        Double raiz2 = 7d;

        System.out.println(raiz1);
        System.out.println(raiz2);
        
        //Apartado e//
        System.out.println("Declarando la variable Contador");

        long contador = 23432342;

        System.out.println(contador);

        //Apartado f//

        System.out.println("Declarando la variable indicador");

        short indicador = 34;

        System.out.println(indicador);

        //Apartado g//
        System.out.println("Declarando la variable indice");

        int indice = 34;

        System.out.println(indice);

        //Apartado h//
        System.out.println("Declarando las variables precio, preciofinal");

        Double precio = 123.5d;
        Double preciofinal = 344.3d;

        System.out.println(precio);
        System.out.println(preciofinal);

        //Apartado i//

        System.out.println("Declarando las variables car1, car2");

        String car1 = "Coche uno";
        String car2 = "Coche dos";

        System.out.println(car1);
        System.out.println(car2);

        //Apartado j//

        System.out.println("Declarando la variable valor");

        byte valor = 3;

        System.out.println(valor);

        //Apartado k-variables l√≥gicos son los booleanos//

        System.out.println("Declarando las variables primero, ultimo");

        boolean primero = true;
        boolean ultimo = false;

        System.out.println(primero);
        System.out.println(ultimo);

        //Apartado l//

        System.out.println("Declarando la variable nombre");

        String nombre = "Marisa Espejo Torres";

        System.out.println(nombre);
        






    }
}
